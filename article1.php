		<h1>Недавно обнаруженный троян маскируется под Adobe Flash Player</h1>
			<img id="img_article" src="img/1428821168_1.jpg" alt="Недавно обнаруженный троян маскируется под Adobe Flash Player" title="Недавно обнаруженный троян маскируется под Adobe Flash Player"><br>
			<p>Недавно обнаруженная разновидность троянской программы для устройств под управлением Android распространяется с помощью фальшивого обновления для Adobe Flash Player, и впоследствии делает устройства более <a href="http://www.welivesecurity.com/2017/02/14/new-android-trojan-mimics-user-clicks-download-dangerous-malware/"> восприимчивыми</a>  к заражению новыми вирусами. <br>

Троян отслеживает пользовательские действия, а так же имитирует их, чтобы зарабатывать деньги на установке мошеннических приложений с рекламой и других вредоносных программ, включая приложения, <a href="http://www.zdnet.com/article/data-stealing-android-ransomware-removed-from-google-play-store/">предназначенные для вымогательства</a>. </p><br>

<article>И как работает этот троян?</article><br>
<p>
Троян, обнаруженный компанией <a href="https://eset.com/">ESET</a> , нацелен на все версии Android и стремится обмануть своих жертв, чтобы те дали им специальные права, позволяющие скачивать приложения. Он распространяется на небезопасных вебсайтах (обычно сайтах для взрослых), используя не вызывающее подозрений окно, которое уведомляет пользователя, что ему надо обновить свою версию Adobe Flash, чтобы обезопасить свое устройство. Если пользователь соглашается, всплывает новый экран, который сообщает ему, что его устройство потребляет слишком много энергии, и пользователь должен включить новый режим экономии энергии. <br>

Вредоносное уведомление подменяет собой настоящую опцию энергосбережения до тех пор, пока пользователь не включит этот режим. <br>

Когда пользователь решает включить этот режим, открывается меню управления доступом, в котором выводится список всех приложений пользователя, а также новый режим энергосбережения. Этот режим запрашивает права на мониторинг пользовательских действий, получение контента и включение аудиоподсказок. Все эти права нужны, чтобы имитировать пользовательские действия и выбирать что-либо на экране, скачивая вредоносные программы. <br>

С момента включения этого сервиса, он прячется от пользователя, продолжая в фоновом режиме контролировать систему. Когда вирус скачивает вредоносные приложения, он активирует ненастоящий экран блокировки без возможности его выключения, чтобы не дать пользователю понять, что его устройство заражено. Экран закрывается сам после того, как вирус завершил все свои действия.</p> <br>

