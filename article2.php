			<p>Google объявила, что команды-победители будут объявлены <a href="https://research.google.com/youtube8m/workshop.html">на семинаре YouTube-8М,</a> , который пройдёт на проводимой организацией IEEE конференции CVPR 2017 в июле. Кроме вкусных призов у особо отличившихся разработчиков будет возможность получить работу в компании. Первым участникам также предлагаются бесплатные аккаунты Google Cloud.</p>

			<p>
